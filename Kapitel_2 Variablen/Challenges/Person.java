/*
* 1. Erstelle ein Programm, welches folgende Werte in den passenden Variablen speichert:
*    Startnummer: 1433
*    Name: "Alex Jones"
*    Startblock: 'a'
*    Zielzeit: 12.45
* 2. Gib die Werte der Variablen auf der Konsole aus.
*/

public class Person {
    public static void main(String[] args) {
        int startNumber = 1433;
        String name = "Alex Jones";
        char startBlock = 'a';
        double targetTime = 2.12;

        System.out.println("Startnummer: " + startNumber);
        System.out.println("Name: " + name);
        System.out.println("Startblock: " + startBlock);
        System.out.println("Zielzeit: " + targetTime);
    }
}